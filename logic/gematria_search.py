def calculate_name_value(name):
    return sum(jumal_map.get(char, 0) for char in name.replace(" ", "").strip())

def find_name_combinations(target_value, max_combination_length=4):
    name_values = [(name, calculate_name_value(name)) for name in asmaa_allah]
    results = []
    for r in range(1, max_combination_length + 1):
        for combo in itertools.combinations(name_values, r):
            if sum(val for _, val in combo) == target_value:
                results.append([name for name, _ in combo])
    return results
